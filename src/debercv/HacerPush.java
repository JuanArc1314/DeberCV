/*
PROCESO PARA HACER PUSH DESDE NETBEANS

1. Abrir el proyecto en NetBeans.

2. Verificar que el proyecto esté conectado a Git.
   - Clic derecho sobre el proyecto.
   - Ir a Git.
   - Si aparecen opciones como Commit, Push, Pull, etc.,
     significa que el proyecto ya está usando Git.

3. Guardar todos los cambios realizados en el proyecto.

4. Hacer Commit de los cambios.
   - Clic derecho sobre el proyecto.
   - Git > Commit.
   - Seleccionar los archivos modificados.
   - Escribir un mensaje de commit.
     Ejemplo:
     "Se agregaron cambios al proyecto"
   - Presionar Commit.

5. Hacer Push al repositorio remoto.
   - Clic derecho sobre el proyecto.
   - Git > Remote > Push.

6. Seleccionar el repositorio remoto.
   - Normalmente aparece como origin.
   - Verificar que la URL sea la del repositorio de GitHub.

7. Seleccionar la rama a subir.
   - Por ejemplo: main, master o develop.
   - Verificar que sea la rama correcta.

8. Ingresar credenciales si NetBeans las solicita.
   - Usuario de GitHub.
   - Token personal de acceso en lugar de contraseña.

9. Confirmar el Push.
   - Presionar Next.
   - Luego Finish.

10. Esperar a que NetBeans termine el proceso.
    - Si no aparece error, el push se realizó correctamente.

11. Verificar en GitHub.
    - Entrar al repositorio.
    - Revisar que los cambios aparezcan en la rama correspondiente.
*/
